_start:
	// 第一步：吧 0x11111111 写入 0xE0200240 位置。

	ldr r0, =0x11111111			// 前两句表示 ldr 是一个伪指令： = 号确定的。 # 表示是一个指令，因为需要编译器来判断这个数是合法立即数还是非法立即数。
	ldr r1, =0xE0200240
	str r0, [r1]				// 寄存器间接寻址。 把 r0 中的数写入到 r1 中的数位地址的内存中去

	// 第二部：向data寄存器写

	ldr r0, =0x0
	ldr r1, =0xE0200244
	ldr r0, [r1]

flag:							// 这两行写了一个死循环。因为裸机程序是直接在CPU上运行的，CPU会逐行运行裸机程序知道CPU断电关机。如果我们的程序所有的代码都执行完了
	b flag						// CPU就会跑飞（跑飞以后是未定义的，所以千万不能让CPU跑飞），不让CPU跑飞的办法就是死循环